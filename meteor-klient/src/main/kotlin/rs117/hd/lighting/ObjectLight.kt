    companion object {
        private var LIGHTS: Map<Int, ObjectLight>? = null
        fun find(id: Int): ObjectLight? {
            return LIGHTS!![id]
        }

        init {
            val builder = ImmutableMap.Builder<Int, ObjectLight>()
            for (objectLight in values()) {
                for (id in objectLight.id) {
                    builder.put(id, objectLight)
                }
            }
            LIGHTS = builder.build()
        }
    }
